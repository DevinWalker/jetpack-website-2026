<?php
/**
 * Shared inline-SVG icon helper.
 *
 * Echoes an SVG from the lucide-react set the theme uses throughout. Consumed
 * by pricing-table, pricing-comparison, pricing-hero, and any other block that
 * needs a matching icon.
 *
 * @package Jetpack_Theme
 */

declare( strict_types=1 );

if ( ! function_exists( 'jetpack_theme_icon' ) ) {
	/**
	 * Render an inline SVG icon.
	 *
	 * @param string $name    Icon name (check, x, shield, zap, trending-up, settings, arrow-right, check-circle).
	 * @param string $classes Utility classes to apply to the <svg> element.
	 *
	 * @return void
	 */
	function jetpack_theme_icon( string $name, string $classes = 'h-5 w-5' ): void {
		$cls = esc_attr( $classes );
		switch ( $name ) {
			case 'check':
				echo '<svg class="' . $cls . '" viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="3" stroke-linecap="round" stroke-linejoin="round" aria-hidden="true"><polyline points="20 6 9 17 4 12"/></svg>';
